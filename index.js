var exports = module.exports;
exports.multiplication = function(num1, num2) {
  return num1 * num2;
};

exports.addition = function(num1, num2) {
  return num1 + num2;
};

exports.subtraction = function(num1, num2) {
  return num1 - num2;
};

exports.division = function(num1, num2) {
  return num1 / num2;
};